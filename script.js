function toAdd(a, b) {
    return a + b;
}

function toSubtract(a, b) {
    return a - b;
}

function toMultiply(a, b) {
    return a * b;
}

function toDivide(a, b) {
    return a / b;
}

toAdd(10, 2); // Will return 12
toSubtract(10, 2); // Will return 8
toMultiply(10, 2); // Will return 20
toDivide(10, 2); // Will return 5
